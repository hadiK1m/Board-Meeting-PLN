export const extractCode = (text: string) => {
    // Mencari semua teks di dalam kurung
    const matches = text.match(/\(([^)]+)\)/g);
    if (matches) {
        // Ambil matches terakhir, hilangkan tanda kurungnya
        const lastMatch = matches[matches.length - 1];
        return lastMatch.substring(1, lastMatch.length - 1);
    }
    return text; // Jika tidak ada kurung, kembalikan teks utuh
};

